func numTilings(N int) int {
	/*
		è¾“å…¥ 3 è¾“å‡º  5

		dp[i] ä¸º æ­å»ºæ–¹æ¡ˆç§æ•°
		dp[0] = 0 dp[1] =1 dp[2]=2 dp[3] = 5

		dp[i] = max(dp[i-j] * dp[j]) 0<j<i
	*/

	dp := make([]int,N+1)
	dp[0],dp[1],dp[2],dp[3] = 0,1,2,5

	for i:=4;i<N+1;i++ {
		for j:=1;j<i;j++ {
			dp[i] = max(dp[i],dp[i-j]*dp[j])
		}
	}

	return dp[N]
}
func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}


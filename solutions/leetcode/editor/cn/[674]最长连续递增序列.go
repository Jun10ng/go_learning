func findLengthOfLCIS(nums []int) int {
	/*
			dp[i] = ä»¥ç¬¬iä¸ªå…ƒç´ ç»“æŸçš„æœ€å¤§å­åºåˆ—é•¿åº¦
			dp[i] = dp[i-1]
			1,3,5,4,7
		  0 1 2 3 1 2
	*/
	if len(nums)==0 {
		return 0
	}
	dp := make([]int,len(nums)+1)
	dp[1]=1
	maxL := 1
	for i:=1;i<len(nums);i++ {
		if nums[i] > nums[i-1] {
			dp[i+1] = dp[i]+1
		}else {
			dp[i+1]=1
		}
		maxL =max(dp[i+1],maxL)
	}
	return maxL
}
func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}


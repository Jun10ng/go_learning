func isMatch(s string, p string) bool {
	p = prehandle(p)
	trace := func(s,p string)bool {
		return false
	}
	trace = func(s, p string) bool {
		if len(p)==0 {
			return len(s)==0
		}
		flag := len(s)!=0 && (s[0]==p[0] || p[0]=='?')
		if p[0]=='*' {
			if len(s)==0 {
				return isAllStart(p)
			}
			return trace(s[1:],p)|| trace(s,p[1:])
		}else {
			return flag && trace(s[1:],p[1:])
		}
	}
	return trace(s,p)
}
func isAllStart(s string)bool {
	for _,e := range s{
		if e!='*' {
			return false
		}
	}
	return true
}
// å»æ‰å¤šä½™çš„*å·
func prehandle(p string) string {
	ans := ""
	l := len(p)
	for i:=0;i<l;i++{
		if i!=l-1 &&p[i]=='*'&&p[i+1]=='*' {
			continue
		}
		ans = ans+string(p[i])
	}
	return ans
}
